package com.keremboz.saglikapp;

public class Asi {
    private String ilac;
    private String zaman;

    public Asi() {

    }

    public Asi(String ilac, String zaman) {
        this.ilac = ilac;
        this.zaman = zaman;
    }

    public String getIlac() {
        return ilac;
    }

    public String getZaman() {
        return zaman;
    }
}
